package Switch;

public class Switch_2 {

	public static void main(String[] args) {

		long z=10;
		switch(z) {
		case 10: 
			System.out.println("Monday");
		case 11: 
			System.out.println("Tuesday");
		case 12: 
			System.out.println("Wednesday");
		default:
			System.out.println("Friday");
		}

	}

}
